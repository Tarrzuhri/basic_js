// operator aritmatika //
let t = 10;
let u = 5;
console.log(t + u); // Output: 15
console.log(t - u); // Output: 5
console.log(t * u); // Output: 50
console.log(t / u); // Output: 2
console.log(t % u); // Output: 0

// operator penugasan //
let r = 10;
let s = 5;
r += s; // sama dengan r = r + s
console.log(r); // Output: 15

// operator perbandingan //
let q = 10;
let w = 5;
console.log(q > w); // Output: true
console.log(q < w); // Output: false
console.log(q == w); // Output: false
console.log(q != w); // Output: true

// operator logika //
let o = 10;
let p = 5;
let j = 20;
console.log(o > p && o < j); // Output: true
console.log(o > p || o > j); // Output: true
console.log(!(o > p)); // Output: false

// operator pembandingan tipe data //
let k = 10;
let v = "10";
console.log(k === v); // Output: false
console.log(k !== v); // Output: true

// operator ternary //
let n = 10;
let m = 5;
let nm = n > m ? "n lebih besar dari m" : "y lebih besar dari x";
console.log(nm); // Output: n lebih besar dari m

